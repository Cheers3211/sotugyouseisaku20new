投稿規約（ドラフト）
最終更新日：2025年10月4日
本規約は、【サイト名／運営者名】（以下「当運営」）が提供するウェブサイト【サイト名・URL】（以下「本サイト」）にイベント情報（以下「投稿内容」）を投稿するすべての方（以下「投稿者」）に適用されます。投稿者は、本規約に同意のうえ投稿してください。
1. 目的
本規約は、投稿内容の適正な掲載と、利用者に対する有益で安全な情報提供を目的とします。
2. 投稿資格
投稿者は、イベントの主催者本人、または主催者から正式に委託・許諾を受けた者であること。
未成年者は、保護者等の同意を得たうえで投稿してください。
3. 投稿可能な内容
日時・場所・参加条件・料金・主催者名・問い合わせ先・公式URL・画像・概要説明 等。
投稿内容は、事実に基づき、最新かつ正確であるよう努めてください。
4. 禁止事項
以下に該当・類似する内容の投稿を禁止します。
法令・条例・公序良俗に反する内容、差別・誹謗中傷・ハラスメント・暴力的表現
虚偽・誤認を与える情報、ねずみ講・マルチ商法・過度な勧誘
第三者の知的財産権・肖像権・プライバシー権を侵害する内容
アダルト、出会い、ギャンブル、薬物、武器等、当運営が不適切と判断する内容
ウイルス・不正プログラム・スパム等の配布や誘導
法令・会場規約・プラットフォーム規約に違反する行為の助長
5. 権利帰属・利用許諾
投稿内容に関する著作権・その他一切の権利は、原則として投稿者または正当な権利者に帰属します。
投稿者は当運営に対し、本サイトおよび当運営のSNS・メール・広告・アプリ等で、投稿内容（テキスト・画像・サムネイル等）を無償で複製・翻案・公開・編集・要約・配信する非独占的・世界的・サブライセンス不可の利用許諾を付与します。
当運営は、掲載レイアウト最適化・検索用タグ付け・要約・トリミング・画像の変換/圧縮等の編集を行うことがあります。
6. 画像・肖像・著作物の取り扱い
投稿者は、アップロードまたはURL指定する画像・ロゴ・地図・動画等について、掲載に必要な権利（著作権・肖像権・商標権・利用許諾等）を適法に取得していることを保証します。
第三者権利侵害の申出があった場合、当運営は、当該投稿の非公開・削除・差替え等の措置を行うことがあります。
7. 個人情報の取り扱い
当運営は、投稿者が入力した連絡先やアカウント情報を、本サイトの運営・問い合わせ対応・不正防止の目的で適切に取り扱います。詳細は【プライバシーポリシーURL】をご確認ください。
イベントページに表示される主催者名・連絡先は、投稿者の設定に基づき公開されます。
8. 掲載審査・掲載方針
当運営は、投稿内容を審査し、掲載可否・掲載時期・掲載順を独自に判断します。不掲載・一時停止・削除の理由は必ずしも開示しません。
掲載後であっても、事実不整合・権利侵害の疑い・苦情・不適切性が判明した場合、通知なく修正・非公開・削除することがあります。
9. 内容の変更・中止・キャンセル
イベント内容（日時・会場・料金・出演者等）に変更が生じた場合、投稿者は速やかに更新してください。
中止・延期となった場合も、速やかに変更を反映してください。
更新遅延や情報不備によるトラブルについて、当運営は責任を負いません。
10. 表明保証
投稿者は、以下を保証します。
投稿内容が真実かつ正確であり、適用法令および第三者権利を侵害しないこと
掲載に必要な全ての権利・許可・同意を保有していること
本規約および当運営の指示・ガイドラインに従うこと
11. 免責
当運営は、投稿内容の正確性・完全性・有用性について、明示または黙示のいかなる保証もしません。
本サイトの利用・掲載・不掲載・変更・中止等により生じたいかなる損害についても、当運営は一切の責任を負いません（当運営に故意または重過失がある場合を除く）。
12. 損害賠償・補償
投稿者の故意・過失により当運営または第三者に損害が生じた場合、投稿者はその一切の損害（合理的な弁護士費用含む）を賠償・補償するものとします。
13. 利用停止・アカウント
本規約違反・不正行為・不適切な投稿が確認された場合、当運営はアカウントの停止・投稿の削除等の措置を行うことがあります。
14. 規約の変更
当運営は、必要に応じて本規約を変更できます。変更後の規約は、本サイトに掲示した時点から効力を生じるものとします。
15. 準拠法・裁判管轄
本規約は日本法に準拠します。
本サイトまたは本規約に関連して生じた紛争について、【運営所在地】を管轄する【○○地方裁判所】を第一審の専属的合意管轄とします。